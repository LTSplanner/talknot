"""Gemini へ渡す評価プロンプトの組み立て。

評価項目は config.settings.EVALUATION_CRITERIA を唯一の真実として参照する。
出力は core.models.EvaluationResult と対応する JSON に固定する。
"""
from __future__ import annotations

from config import settings


def build_evaluation_prompt(
    reference_talk: str | None = None,
    knowledge_base: str | None = None,
) -> str:
    criteria_lines = "\n".join(
        f"- {c.key} ({c.number} {c.title}): {c.description}"
        for c in settings.EVALUATION_CRITERIA
    )
    reference_block = (
        f"\n# 模範トーク（社内基準）\n{reference_talk}\n" if reference_talk else ""
    )
    knowledge_block = (
        "\n# 弊社ナレッジ（過去の商談から蓄積した社内知識。これを前提に評価する）\n"
        f"{knowledge_base}\n"
        if knowledge_base
        else ""
    )
    reference_axis_note = (
        "上の『模範トーク』の型・流れ・言い回し・技術をどれだけ再現できたか（似ているか）で採点する。"
        if reference_talk
        else "住宅営業の基本の型（あいさつ→ヒアリング→提案→次の一歩）にどれだけ沿えたかで採点する（模範トーク未登録のため基本の型を基準）。"
    )
    return f"""{settings.SALES_AI_PERSONA}

動画（または音声）から、文字面だけでなく「声のトーン」「間」「発話比率」、さらに
言い淀み・沈黙・話題の回避・過剰な同意・即答できない様子・声の小ささなどの
非言語サインを手がかりに、お客様が言葉にしていない本音・不安・疑問（秘密領域）まで
読み取ってください。
{reference_block}{knowledge_block}
# 最重要：まずお客様の『隠れたニーズ（秘密領域）』を読む
- お客様の立場に立ち、商談中に「口には出していないが感じていたであろう」不安・疑問・
  期待を推定する（お客様自身もまだ自覚していないものを含む）。例：予算オーバーへの
  本当の不安、家族（親・子・実家）への遠慮、決断を先延ばしにしたい心理、本当は譲れない条件。
- 推定の根拠は必ず非言語サイン（トーン変化・間・言い淀み・話題そらし・愛想笑い等）に紐づける。
- そのうえで、営業がそのニーズに気づいて踏み込めたか／表面の言葉だけで流したかを見る。
- この「隠れたニーズへの踏み込み」の深さを、採点の中心に置く。

# 採点の基準（厳格に。お客様目線で、甘い点は付けない）
各項目を 1〜5 で、次のアンカーに従って厳しく採点する：
- 1：お客様の本音・不安をほぼ拾えず、表面的なやり取りに終始。
- 2：一部は拾えたが、肝心の“裏のニーズ”を取りこぼした。
- 3：表面のニーズには対応できたが、言葉にされない不安・疑問への踏み込みは不十分（標準）。
- 4：言葉にされていない不安・疑問を察知し、的確に踏み込めた（明確な根拠がある時のみ）。
- 5：お客様自身も気づいていなかったニーズまで言語化し、信頼と期待を一段引き上げた（稀）。
重要な注意：
- 会話が滑らかでも、裏のニーズを取りこぼしていれば高得点にしない（雰囲気の良さに惑わされない）。
- 大半の実商談は 2〜3 に収まる。4〜5 は具体的なタイムスタンプの根拠がある場合のみ。
- reference・sales の両軸とも、このアンカーで厳格に採点する。

# 評価の2軸（必ず“別の物差し”で 1〜5 採点する。安易に同じ点にしない）
- reference（🎯 模範トーク視点）＝再現度：{reference_axis_note}
- sales（💼 営業プロ視点）＝本質的な質：模範に関係なく、お客様目線で見たトークそのものの完成度。
※この2軸は測るものが違うため、点数は一致しないことが多い。例：
  ・模範とは違うやり方だが上手い → sales 高め / reference 低め。
  ・模範の型はなぞれたが、目の前のお客様には響いていない → reference 高め / sales 低め。
  両軸を機械的に同じ数字にしないこと。reference_comment と sales_comment は別々の根拠で書く。

# 評価項目
{criteria_lines}

# 出力フォーマット（JSON のみ。前後に説明文を付けない）
{{
  "hidden_needs": [{{
    "timestamp": "MM:SS",
    "signal": "<根拠となった非言語サイン（間・トーン・言い淀み・話題回避など）>",
    "inferred_need": "<お客様自身も言語化していない不安・疑問・本音>",
    "surfaced": <true=営業が気づき踏み込めた / false=表面で流した>,
    "note": "<踏み込めた点、または本来どう触れて深掘りすべきだったか>"
  }}],
  "scores": [{{
    "key": "<上記key>",
    "reference_score": <1-5>,
    "reference_comment": "<模範トーク視点での講評（近い点・差分・学ぶ点）>",
    "sales_score": <1-5>,
    "sales_comment": "<営業プロ視点での講評（できている点・次の一手）>"
  }}],
  "feedback": [{{
    "timestamp": "MM:SS",
    "criterion_key": "<関連する評価項目key>",
    "emotion_note": "<その瞬間のお客様の感情の動き。可能なら隠れたニーズと結びつける>",
    "before": "<実際のトーク。裏のニーズをスルーしていた箇所>",
    "after": "<本来こう言うべきだった、の具体的な改善トーク例>"
  }}],
  "summary": "<2軸を踏まえた全体のポジティブな振り返り（言葉は前向きに）>",
  "knowledge": [{{
    "category": "<product=商品知識 / rule=社内ルール / technique=トーク技術 のいずれか>",
    "point": "<この商談から学べる、他の商談にも使える一般的な知識・ルール・コツ>"
  }}]
}}

hidden_needs は最重要項目です。商談の核心となる『隠れたニーズ』を 2〜5 件、必ず根拠
（signal）付きで出してください。根拠のない憶測は書かない。
各項目について reference と sales の両方のスコア・講評を必ず出し、上のアンカーで厳格に採点してください。
feedback は必ず動画内の具体的なタイムスタンプ付きで、Before/After をセットで出してください。
講評（コメント）の“言葉”は前向き・建設的に、しかし“点数”は甘くしないこと。

knowledge には、この商談から抽出できる『弊社の財産になる知識』を 0〜5 件入れてください。
- product（商品知識）/ rule（社内ルール）/ technique（トーク技術）の3カテゴリで、学びがあるものだけ。
- 必ず一般化して書く（次の商談でも使える形に）。特筆すべき学びが無ければ空配列 [] でよい。
- 個人情報は絶対に含めない（顧客名・住所・電話番号・金額などの個別具体情報は書かない）。"""
